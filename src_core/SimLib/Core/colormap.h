		g = viridis_cm[scale][1];
		b = viridis_cm[scale][2];
		break;
	case PARULA_COLORMAP:
		r = parula_cm[scale][0];
		g = parula_cm[scale][1];
		b = parula_cm[scale][2];
		break;
	case AUTUMN_COLORMAP:
		r = autumn_cm[scale][0];
		g = autumn_cm[scale][1];
		b = autumn_cm[scale][2];
		break;
